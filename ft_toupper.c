#include <stdio.h>

int	toupper(int c)
{
	if(c >= 97 && c <= 122)
	{
		return c - 32;
	}
	else
	{
		return c;
	}
}

int	main()
{
	printf("%c\n", toupper('#'));
	return 0;
}
