#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	void		*original_dest;
	char		*d;
	const char	*s;

	original_dest = dest;
	d = (char *)dest;
	s = (const char *)src;
	if (!dest && !src)
		return (NULL);
	if (d < s)
	{
		while (n--)
			*d++ = *s++;
	}
	else
	{
		d += n;
		s += n;
		while (n--)
		{
			*--d = *--s;
		}
	}
	return (original_dest);
}
